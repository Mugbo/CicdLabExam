public class Employee {

    private String name;
    private long id;
    private String employmentType;
    private String gender;
    private int age;

    public Employee(String name, long id, String employmentType, String gender, int age){
    setName(name);
    setId(id);
    setEmployment(employmentType);
    setGender(gender);
    setAge(age);
    }



}
